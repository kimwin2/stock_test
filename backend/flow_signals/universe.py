def classify_sector(stock_name: str, code: str | None = None) -> str:
    if code and code in EXPLICIT_SECTOR:
        return EXPLICIT_SECTOR[code]
    name = stock_name or ""
    upper = name.upper()
    for sector, keywords in SECTOR_RULES:
        for kw in keywords:
            if kw in name or kw.upper() in upper:
                return sector
    return "기타"


def build_universe(top_n_kospi: int = 400, top_n_kosdaq: int = 200) -> pd.DataFrame:
    """시가총액 상위 종목 유니버스 + 섹터 라벨.

    Returns DataFrame with columns: code, name, market, marketCap, sector
    """
    kospi = fetch_kospi_listing()
    kosdaq = fetch_kosdaq_listing()

    # 시가총액 컬럼명 정규화
    def _normalize(df: pd.DataFrame, market: str, top: int) -> pd.DataFrame:
        cap_col = next((c for c in ["Marcap", "MarCap", "시가총액", "marketCap"] if c in df.columns), None)
        if cap_col is None:
            raise ValueError(f"market cap column not found in {market}")
        name_col = next((c for c in ["Name", "Symbol Name", "종목명"] if c in df.columns), None)
        code_col = next((c for c in ["Code", "Symbol", "종목코드"] if c in df.columns), None)

        df = df.copy()
        df = df.rename(columns={code_col: "code", name_col: "name", cap_col: "marketCap"})
        df = df[["code", "name", "marketCap"]].dropna(subset=["code", "name"])
        df["code"] = df["code"].astype(str).str.zfill(6)
        df = df.sort_values("marketCap", ascending=False).head(top).reset_index(drop=True)
        df["market"] = market
        df["sector"] = [classify_sector(n, c) for n, c in zip(df["name"], df["code"])]
        return df

    kospi_top = _normalize(kospi, "KOSPI", top_n_kospi)
    kosdaq_top = _normalize(kosdaq, "KOSDAQ", top_n_kosdaq)
    universe = pd.concat([kospi_top, kosdaq_top], ignore_index=True)
    return universe
